def prompt_and_print():
    num1 = int(input("Enter An Number: "))
    num2 = int(input("Enter Another Number: "))


    print(f"{num1} + {num2} = {num1 - num2}")
    print(f"{num1} - {num2} = {num1 - num2}")
    print(f"{num1} * {num2} = {num1 * num2}")
    print(f"{num1} / {num2} = {num1 / num2}")

if __name__ == "__main__":
    prompt_and_print()
